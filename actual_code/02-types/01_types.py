my_favourite_number = 42

print("Hello world!")
print(my_favourite_number)  # int -> whole number/ Integer
print(type(my_favourite_number))

# dynamically typed
my_favourite_number = 4.2
print(my_favourite_number) # float -> decimal 
print(type(my_favourite_number))

my_favourite_number = "forty two"
print(my_favourite_number) # str -> string  
print(type(my_favourite_number))


my_favourite_number = True   # False
print(my_favourite_number) # bool -> boolean   (my_favourite_number > 10)
print(type(my_favourite_number))

my_favourite_number = None
print(my_favourite_number) # NoneType  (null, undefined, void)
print(type(my_favourite_number))